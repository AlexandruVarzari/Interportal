package Lesson3;

public class Loops {
    public static void main(String[] args) {

        int i  = 10;
        while (i < 15){
            System.out.println(i);
            i++;
        }

        int j = 10;
        do{
            j++;
            System.out.println(j);
        }
        while (j < 15);
    }
}
